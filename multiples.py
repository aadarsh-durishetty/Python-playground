# multiples and inputs based on odd/even

n = int(input())

for i in range(n):
      num = n*i
      if num%2 == 0:
        pass
        if n%2 == 0:
           print('even multiples of even n: ',num)

for i in range(n):
      num = n*i
      if num%2 == 1:
        pass
        if n%2 == 1:
           print('odd multiples of odd n: ',num)      

    